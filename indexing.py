#indexing strings using the [] operator
# index value must be an integer and start with a zero
#index value can be an expression that is computed
a="Patrick"
print(a[2:3])
print(a[5:7])
print(a[1:5])
print(a[0:5])

s="Hello world"
print(s[3:7])
print(s[2:4])
print(s[:])
print(s[:5])
print(s[-1:-3])
print(s[1])
print(s[::2])
